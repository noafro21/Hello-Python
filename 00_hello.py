print ("La multiplicación de 6 y 5 es: ", (6 * 5)) # Multiplicación(*)
print ("La potencia de 6 elevado a 2 es: ", (6 ** 2)) # Potencia o Exponencial(**)
print ("El módulo de  6 y 3 es: ", (6 % 3)) # Módulo(%)
